                    <h1>SOBRE</h1>
                    <p>
                        O Classificador Indicativo para Histórias em Quadrinhos (beta) é uma ferramenta desenvolvida
                        pelo quadrinista Rubens Menezes para auxiliar colegas quadrinistas a determinarem a
                        classificação indicativa de suas obras.
                    </p>
                    <p>
                        O Classificador Indicativo para Histórias em Quadrinhos funciona de maneira bastante simples: o
                        autor seleciona as principais características presentes (ou ausentes) em sua obra e, ao clicar
                        no botão "CLASSIFICAR", obtém uma sugestão de classificação indicativa.
                    </p>
                    <p>
                        Esta sugestão de classificação é baseada nos critérios do Ministério da Justiça e outras fontes
                        consultadas para a criação deste sistema. Os resultados trarão ainda uma explicação concisa
                        daquela classificação e como foi que o sistema a determinou, consideradas as características da
                        obra indicadas pelo usuário, permitindo que tudo seja duplamente checado.
                    </p>
                    <p>
                        É importante notar que essa ferramenta não pretende oferecer aconselhamento legal, nem é
                        infalível na sua determinação de faixas etárias, estando a decisão final de a utilizar, ou não,
                        a critério do bom senso do usuário.
                    </p>
